package CH6.Exercise;

public class Printer {
    void println(int i) {
        System.out.println(i);
    }

    void println(boolean x) {
        System.out.println(x);
    }

    void println(double r) {
        System.out.println(r);
    }

    void println(String str) {
        System.out.println(str);
    }
}
